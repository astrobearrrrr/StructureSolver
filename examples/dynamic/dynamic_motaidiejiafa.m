K=[3,-1;-1,1]*2;
M=[30,0;0,10]/4;%[6,1;1,2]/12;%;
[P,D]=eig(K,M)
omega1=sqrt(D(1,1));
omega2=sqrt(D(2,2));
t=0:0.12:1.2;
t0=0.5;
for i=1:length(t)
    if(t(i)<=t0)
    x(1,i)=P(2,1)*1/(omega1^2)*(1-cos(omega1*t(i)));
    x(2,i)=P(2,2)*1/(omega2^2)*(1-cos(omega2*t(i)));
    else
    x(1,i)=P(2,1)*1/(omega1^2)*(-cos(omega1*t(i))+cos(omega1*(t(i)-t0)));
    x(2,i)=P(2,2)*1/(omega2^2)*(-cos(omega2*t(i))+cos(omega2*(t(i)-t0)));
    end
end

u=P*x;

